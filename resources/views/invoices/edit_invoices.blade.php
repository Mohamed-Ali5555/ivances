    <script>
        function myFunction() {
            var Amount_Commission = parseFloat(document.getElementById('Amount_Commission')
                .value); //there give the value of مبلغ العمولة

            var Discount = parseFloat(document.getElementById('Discount').value);

            var Rate_VAT = parseFloat(document.getElementById('Rate_VAT').value); //نسبه الضريبه
            var Value_VAT = parseFloat(document.getElementById('Value_VAT').value); // ظقيمة ضريبة القيمة المض

            var Amount_Commission2 = Amount_Commission - Discount; //مبلغ العموله ناقص الخصم

            if (typeof Amount_Commission === 'undefined' || !Amount_Commission) {
                alert('يرجي ادخال مبلغ العمولة');
            } else {
                var intResults = Amount_Commission2 * Rate_VAT / 100; // THIS == VALUE_VATE

                var intResults2 = parseFloat(intResults + Amount_Commission2); //total

                sumq = parseFloat(intResults).toFixed(2);

                sumt = parseFloat(intResults2).toFixed(2);

                document.getElementById("Value_VAT").value = sumq;

                document.getElementById("Total").value = sumt; //ارقام عشريه

            }

        }
    </script>


